a = 'Slice me!'

# Get the characters from position 0 to position 2 (not included)
print(a[0:2])

# Slice from start

# Get the characters from the start to position 5 (not included):
print(a[:5])

# Slice to the End

# Get characters from position 2 till the end of the string
print(a[2:])


# Negative indexing

"""
Get the characters:

From: "m" in "Slice me!" (position -3)

To, but not included: "!" in "World!" (position -1):
"""

print(a[-3:-1])